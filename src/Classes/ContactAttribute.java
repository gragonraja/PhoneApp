package Classes;

import android.R.string;

public class ContactAttribute 
{
	// Attribute for FirstName.
	private string firstName;
	// Setter.
	public void setFirstName(string value)
	{
		firstName = value;
	}
	// Getter.
	public string getFirstName(string value)
	{
		return firstName;
	}
	
	// Attribute for LastName.
	private string lastName;
	// Setter.
	public void setLastName(string value)
	{
		lastName = value;
	}
	// Getter.
	public string getLastName(string value)
	{
		return lastName;
	}
	
	// Attribute for PhoneNumber.
	private string phoneNumber;
	// Setter.
	public void setPhoneNumber(string value)
	{
		phoneNumber = value;
	}
	// Getter.
	public string getPhoneNumber(string value)
	{
		return phoneNumber;
	}
	
	// Attribute for HomeNumber.
	private string homeNumber;
	// Setter.
	public void setHomeNumber(string value)
	{
		homeNumber = value;
	}
	// Getter.
	public string getHomeNumber(string value)
	{
		return homeNumber;
	}
	
	// Attribute for WorkNumber.
	private string workNumber;
	// Setter.
	public void setWorkNumber(string value)
	{
		workNumber = value;
	}
	// Getter.
	public string getWorkNumber(string value)
	{
		return workNumber;
	}
}
